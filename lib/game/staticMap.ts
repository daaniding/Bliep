/** Parse raw ASCII → number grid (0=water, 3=grass). River removed — all land is grass. */
export function parseElevation(): number[][] {
  const lines = ELEVATION_TEXT.trim().split('\n');
  const rows: number[][] = [];
  for (const raw of lines) {
    const padded = raw.padEnd(MAP_COLS, '.');
    const row: number[] = [];
    for (let i = 0; i < MAP_COLS; i++) {
      const ch = padded[i];
      // Both '1' and 'r' become grass — river removed for cleaner look
      if (ch === '1' || ch === 'r') row.push(3);
      else row.push(0);                 // water
    }
    rows.push(row);
  }
  while (rows.length < MAP_ROWS) rows.push(new Array(MAP_COLS).fill(0));
  return rows.slice(0, MAP_ROWS);
}

/**
 * Post-process the raw elevation grid.
 *
 * Adds shallow water gradient + sand beach band for island feel.
 *   0 = deep water
 *   1 = shallow water (2-3 tiles from coast)
 *   2 = sand/beach (1-2 tile band around ocean coastline)
 *   3 = grass (buildable land)
 *   4 = lake water (inland, no sand band, coast autotile banks)
 */
export function processElevation(raw: number[][]): number[][] {
  const rows = raw.length;
  const cols = raw[0]?.length ?? 0;

  const grid: number[][] = raw.map(r => [...r]);

  // ---- BFS: distance from every cell to nearest land ----
  const distToLand: number[][] = Array.from({ length: rows }, () =>
    new Array(cols).fill(999)
  );
  const q2: Array<[number, number]> = [];
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      if (grid[r][c] >= 2) { distToLand[r][c] = 0; q2.push([r, c]); }
    }
  }
  let i2 = 0;
  while (i2 < q2.length) {
    const [r, c] = q2[i2++];
    const d = distToLand[r][c];
    if (d >= 4) continue;
    for (const [dr, dc] of [[-1, 0], [1, 0], [0, -1], [0, 1]]) {
      const nr = r + dr, nc = c + dc;
      if (nr < 0 || nr >= rows || nc < 0 || nc >= cols) continue;
      if (distToLand[nr][nc] <= d + 1) continue;
      distToLand[nr][nc] = d + 1;
      q2.push([nr, nc]);
    }
  }

  // Shallow water: ocean cells within 3 tiles of land
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      if (grid[r][c] === 0 && distToLand[r][c] <= 3) {
        grid[r][c] = 1;
      }
    }
  }

  // ---- Sand beach band: 2 tiles wide around OCEAN coastline ----
  const dirs: Array<[number, number]> = [[-1, 0], [1, 0], [0, -1], [0, 1]];

  // Pass 1: grass cells directly adjacent to ocean water/shallow → sand
  const snap1 = grid.map(r => [...r]);
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      if (snap1[r][c] !== 3) continue;
      for (const [dr, dc] of dirs) {
        const nr = r + dr, nc = c + dc;
        if (nr < 0 || nr >= rows || nc < 0 || nc >= cols) { grid[r][c] = 2; break; }
        if (snap1[nr][nc] <= 1) { grid[r][c] = 2; break; }
      }
    }
  }

  // Pass 2: grass cells adjacent to new sand → also sand (2-tile band)
  const snap2 = grid.map(r => [...r]);
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      if (snap2[r][c] !== 3) continue;
      for (const [dr, dc] of dirs) {
        const nr = r + dr, nc = c + dc;
        if (nr < 0 || nr >= rows || nc < 0 || nc >= cols) continue;
        if (snap2[nr][nc] === 2) { grid[r][c] = 2; break; }
      }
    }
  }

  // ---- Lake in the northeast quadrant ----
  // Organic oval ~11×8 tiles. Elevation 4 = lake water.
  // Grass borders lake directly (no sand) → coast autotile gives cliff banks.
  const lakeCR = 70, lakeRR = 24;
  for (let dr = -4; dr <= 4; dr++) {
    for (let dc = -6; dc <= 6; dc++) {
      // Oval: wider than tall
      const dist = Math.sqrt(dr * dr * 1.8 + dc * dc);
      if (dist > 5.2) continue;
      const r = lakeRR + dr, c = lakeCR + dc;
      if (r >= 0 && r < rows && c >= 0 && c < cols && grid[r][c] === 3) {
        grid[r][c] = 4;
      }
    }
  }

  return grid;
}
